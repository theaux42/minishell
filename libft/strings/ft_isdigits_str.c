#include "libft.h"

int	ft_isdigits_str(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!ft_isdigit(str[i]) && (str[i] != '-' && str[i] != '+'))
			return (0);
		i++;
	}
	return (1);
}